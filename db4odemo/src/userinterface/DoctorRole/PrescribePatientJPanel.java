    private void addToListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToListActionPerformed
        // TODO add your handling code here:
        this.prescription = encounter.getPresciption().addPrescription();
        int frequency = 0;
        int duration = 0;
        if (txtFrequency.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter frequency of medicine");
        }
        if (txtDuration.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Duration of medicine");
        }
        try {
            frequency = Integer.parseInt(txtFrequency.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter Frequency in correct format");
        }
        try {
            duration = Integer.parseInt(txtDuration.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter duration in correct format");
        }

        prescription.setNoofTimesInaday(frequency);
        prescription.setTotalDays(duration);
        prescription.setMedicineName((String) MedicineComboBox.getSelectedItem());
        populatePrescriptionTable(encounter.getPresciption());


    }//GEN-LAST:event_addToListActionPerformed

