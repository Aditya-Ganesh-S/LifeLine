    private void populatePrescriptionTable(PrescriptionList prescriptionlist) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Prescription p : prescriptionlist.getPrescriptionList()) {
            Object[] row = new Object[3];
            row[0] = p.getMedicineName();
            row[1] = p.getNoofTimesInaday();
            row[2] = p.getTotalDays();

            model.addRow(row);
        }

    }

    private void populateMedicines(Enterprise en) {
        for (Map.Entry mapElement : en.getDrugList().entrySet()) {
            String key = (String) mapElement.getKey();
            MedicineComboBox.addItem(key);
        }
    }
