    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof PatientRegistrationRequest) {
                Object[] row = new Object[model.getColumnCount()];
                row[0] = wr;
                row[1] = ((PatientRegistrationRequest) wr).getName();
                row[2] = ((PatientRegistrationRequest) wr).getNumber();
                row[3] = ((PatientRegistrationRequest) wr).getGender();
                row[4] = ((PatientRegistrationRequest) wr).getAge();
                row[5] = ((PatientRegistrationRequest) wr).getMailId();
                row[6] = ((PatientRegistrationRequest) wr).getAddress().getStreetAddress();
                row[7] = ((PatientRegistrationRequest) wr).getAddress().getCity();
                row[8] = ((PatientRegistrationRequest) wr).getAddress().getState();
                row[9] = ((PatientRegistrationRequest) wr).getAddress().getZipcode();
                row[10] = ((PatientRegistrationRequest) wr).getUserName();
                row[11] = ((PatientRegistrationRequest) wr).getStatus();

                model.addRow(row);

            }

        }
    }
