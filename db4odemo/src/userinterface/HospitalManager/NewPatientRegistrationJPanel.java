        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        int count = jTable1.getSelectedRowCount();
        if (count == 1) {
            PatientRegistrationRequest wr = (PatientRegistrationRequest) jTable1.getValueAt(selectedRow, 0);
            String name = wr.getName();
            String number = wr.getNumber();
            Address add = wr.getAddress();
            int age = wr.getAge();
            String gender = wr.getGender();
            String email = wr.getMailId();
            String userName = wr.getUserName();
            String password = wr.getPassword();
            wr.setStatus("Patient Approved");
            Patient patient = enterprise.getPatientList().addToPatientDirectory(name, number, add, age, gender, email);

            UserAccount ua = enterprise.getUserAccountDirectory().createPatientAccount(userName, password, patient, new PatientRole());
             JOptionPane.showMessageDialog(null, "Processed!!");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }

