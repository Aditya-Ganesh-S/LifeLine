    private void btnAddtoListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddtoListActionPerformed
        // TODO add your handling code here:

        Network network = (Network) ComboBoxNetwork.getSelectedItem();
        Enterprise blEnterprise = (Enterprise) ComboBoxEnterprise.getSelectedItem();
        String medName = (String) ComboBoxMedicineName.getSelectedItem();
        int quantity = (int) SpinnerQtyRqd.getValue();
        BloodRequest bloodRequest = new BloodRequest();
        bloodRequest.setHospital(this.enterprise);
        bloodRequest.setBloodbank(blEnterprise);
        bloodRequest.setBloodGroup(medName);
        bloodRequest.setUnits(quantity);
        bloodRequest.setStatus("Approval Pending");
        bloodRequest.setMessage(msgTxtField1.getText());

        if (this.enterprise.getWorkQueue() == null) {
            this.enterprise.setWorkQueue(new WorkQueue());
        } else {
            this.enterprise.getWorkQueue().getWorkRequestList().add(bloodRequest);

        }
        if (blEnterprise.getWorkQueue() == null) {
            blEnterprise.setWorkQueue(new WorkQueue());
        } else {
            blEnterprise.getWorkQueue().getWorkRequestList().add(bloodRequest);
        }
        populateTable();

    }//GEN-LAST:event_btnAddtoListActionPerformed

