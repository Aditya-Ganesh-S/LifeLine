    private void populateNetworkBox() {
        ComboBoxNetwork.removeAllItems();

        for (Network network : ecosystem.getNetworkList()) {
            ComboBoxNetwork.addItem(network);
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof BloodRequest) {
                Object[] row = new Object[model.getColumnCount()];
                row[0] = wr;
                row[1] = ((BloodRequest) wr).getBloodbank().getName();
                row[2] = ((BloodRequest) wr).getBloodGroup();
                row[3] = ((BloodRequest) wr).getUnits();
                row[4] = ((BloodRequest) wr).getStatus();
                row[5] = ((BloodRequest) wr).getMessage();

                model.addRow(row);

            }

        }
    }

    private void populateEnterpriseComboBox(Network network) {
        ComboBoxEnterprise.removeAllItems();

        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            System.out.println(enterprise.getEnterpriseType().toString());
            if (enterprise.getEnterpriseType().toString().equalsIgnoreCase("BloodBank")) {
                ComboBoxEnterprise.addItem(enterprise);

            }
        }
    }

    private void populateBloodGroups(Enterprise en) {
        for (Map.Entry mapElement : en.getBloodList().entrySet()) {
            String key = (String) mapElement.getKey();
            ComboBoxMedicineName.addItem(key);
        }
    }
