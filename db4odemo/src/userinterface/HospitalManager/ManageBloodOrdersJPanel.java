    private void completeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        int count = jTable1.getSelectedRowCount();
        if (count == 1) {
            BloodRequest bloodReq = (BloodRequest) jTable1.getValueAt(selectedRow, 0);
            bloodReq.setStatus("Completed!");
            for (Map.Entry mapElement : bloodList.entrySet()) {
                String key = (String) mapElement.getKey();
                if (key.equalsIgnoreCase(bloodReq.getBloodGroup())) {
                    int newValue = (Integer) mapElement.getValue() + bloodReq.getUnits();
                    bloodList.replace(key, newValue);
                    populateTable();

                    return;
                }
            }
            bloodList.put(bloodReq.getBloodGroup(), bloodReq.getUnits());
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_completeActionPerformed

