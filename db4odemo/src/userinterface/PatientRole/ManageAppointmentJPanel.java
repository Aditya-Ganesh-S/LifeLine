    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) appointmentTable.getModel();
        model.setRowCount(0);
        for (Network network : ecosystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
                    if (wr instanceof AppointmentRequest) {
                        if (((AppointmentRequest) wr).getPatient() == userAccount.getPatient()) {
                            Object[] row = new Object[model.getColumnCount()];
                            row[0] = ((AppointmentRequest) wr).getNetwork().getName();
                            row[1] = ((AppointmentRequest) wr).getEnterprise().getName();
                            row[2] = ((AppointmentRequest) wr).getDate();
                            row[3] = ((AppointmentRequest) wr).getPatientstatus();

                            model.addRow(row);
                        }

                    }
                }
            }
        }

    }

    private void populateCombobox() {
        networkJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();

        for (Network network : ecosystem.getNetworkList()) {
            networkJComboBox.addItem(network);
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise.getEnterpriseType() == Hospital) {
                    enterpriseTypeJComboBox.addItem(enterprise);
                }
            }
        }

    }

    private void populateEnterpriseComboBox(Network network) {
        enterpriseTypeJComboBox.removeAllItems();

        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise.getEnterpriseType() == Hospital) {
                enterpriseTypeJComboBox.addItem(enterprise);
            }
        }
    }
