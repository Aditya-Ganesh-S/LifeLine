    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        Network network = (Network) networkJComboBox.getSelectedItem();
        Enterprise enterprise = (Enterprise) enterpriseTypeJComboBox.getSelectedItem();
        Date date = jDateChooser1.getDate();
        String strDate = DateFormat.getDateInstance().format(date);
        AppointmentRequest appointmentReq = new AppointmentRequest();
        appointmentReq.setNetwork(network);
        appointmentReq.setPatient(userAccount.getPatient());
        appointmentReq.setEnterprise(enterprise);
        appointmentReq.setDate(date);
        appointmentReq.setPatientstatus("Appointment Request Sent");
        appointmentReq.setHopitalAdminStatus("Please assign a doctor");

        if (enterprise.getWorkQueue() == null) {
            enterprise.setWorkQueue(new WorkQueue());
        } else {
            enterprise.getWorkQueue().getWorkRequestList().add(appointmentReq);
        }
        JOptionPane.showMessageDialog(null, "Appointment request sent succesfully!");
        populateTable();

    }//GEN-LAST:event_submitActionPerformed

